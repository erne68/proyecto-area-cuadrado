# ÁREA DE UN CUADRADO
# Solicitar la longitud del lado al usuario
lado = float(input("Ingresa la longitud del lado del cuadrado: "))

# Calcular el área
area = lado * lado

# Mostrar el resultado
print(f"El área del cuadrado es: {area}")

